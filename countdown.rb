def countdown (number_of_times)
  count = 0
  while count < number_of_times
    puts "#{number_of_times} SECOND(S)!"
    number_of_times -= 1
    end
  return "HAPPY NEW YEAR!"
end

def countdown_with_sleep (number_of_times)
  count = 0
  while count < number_of_times
    puts "#{number_of_times} SECOND(S)!"
    number_of_times -= 1
    end
  return "HAPPY NEW YEAR!"
end
